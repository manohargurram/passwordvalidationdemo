package com.myapp.vo;

public class PasswordRequestVO {

	private String password;
	
	public PasswordRequestVO() {
	}

	public PasswordRequestVO(String password) {
		super();
		this.password = password;
	}



	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((password == null) ? 0 : password.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PasswordRequestVO other = (PasswordRequestVO) obj;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "PasswordRequestVO [password=" + password + "]";
	}
	
	
}
